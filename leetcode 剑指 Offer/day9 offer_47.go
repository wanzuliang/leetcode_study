// 默认代码模版
func maxValue(grid [][]int) int {
    X := len(grid) - 1
    Y := len(grid[0]) - 1
    temp := grid
    for i := X; i >= 0; i-- {
        for j := Y; j >= 0; j-- {
            if i == X && j == Y {
                temp[i][j] = grid[i][j]
            } else if i == X {
                temp[i][j] += grid[i][j+1]
            } else if j == Y {
                temp[i][j] += grid[i+1][j]
            } else {
                if grid[i][j+1] > grid[i+1][j] {
                    temp[i][j] += grid[i][j+1]
                } else {
                    temp[i][j] += grid[i+1][j]
                }
            }
        }
    }
    return temp[0][0]
}


